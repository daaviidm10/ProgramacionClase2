    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_programacion", "root", "");
            PreparedStatement pe = cn.prepareStatement("select * from xogadoras where ID= ?");
            
            pe.setString(1, buscar.getText().trim());
            
            ResultSet re = pe.executeQuery();
            
            if(re.next()){
                nome.setText(re.getString("NombreXogadora"));
                ID.setText(re.getString("ID"));
                demarcacion.setText(re.getString("Demarcacion"));
                goles.setText(re.getString("Goles"));
                
            }else{
                JOptionPane.showMessageDialog(null, "Xogadora non rexistrada");
            }
            
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

