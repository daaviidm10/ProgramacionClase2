    private void mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarActionPerformed
        try {

            DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/bd_programacion", "root", "");
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM xogadoras");
            modelo.setRowCount(0);



            if(rs.next()){

             do{
            String[] fila= {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)};
            modelo.addRow(fila);


        }while(rs.next());
            }
   } catch (SQLException ex) {
            Logger.getLogger(Xogadoras.class.getName()).log(Level.SEVERE, null, ex);

            JOptionPane.showMessageDialog(null, "Erro ao mostrar");

        }
    }//GEN-LAST:event_mostrarActionPerformed

