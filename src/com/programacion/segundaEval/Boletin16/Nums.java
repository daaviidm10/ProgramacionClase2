package boletin16;

/**
 *
 * @author dmama
 */
public class Nums {
    private int numero;

    public Nums() {
    }

    public Nums(int numero) {
        this.numero = numero;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    @Override
    public String toString() {
        return "Numero=" + numero;
    }

}
